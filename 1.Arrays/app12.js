// Modifing arrays
let shoppingList = ["Cheddar Cheese", "2% Milk"];
console.log(shoppingList);

// Modify 2nd item in the list
shoppingList[1] = "Whole Milk";
console.log(shoppingList);

// Adding item in the list and the end example 1
shoppingList[2] = "Tomatoes";
console.log(shoppingList);

// or you can use
shoppingList[shoppingList.length] = "Potatoes";
console.log(shoppingList);

// Changing 1st item in the list
shoppingList[0] = "Butter";
console.log(shoppingList);
