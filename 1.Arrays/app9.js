// Ternary Operator
let num = 7;

if (num === 7) {
  console.log("You are lucky");
} else {
  console.log("Sorry bad luck");
}

// Same code with Ternary operator

num === 7 ? console.log("You are lucky") : console.log("Sorry bad luck");

